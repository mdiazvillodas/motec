            <h1>
            Preguntas más frecuentes
            <br>
            </h1>
            <h2> ¿Puedo cambiar turbos? </h2>
            <p>
            Sí, turbos alternativos pueden caracterizarse a través de la mesa de Aim ​​Boost y
            <acronym title = "proporcional, ganancia Integral y Derivada"> PID </acronym>
            impulsar la configuración de control.
            </p>
            <h2> ¿Puedo utilizar puertas de residuos externos? </h2>
            <p> Si, sin embargo, deben ser caracterizadas a través de los parámetros de control PID impulso. </p>
            <h2> ¿Puedo cambiar las válvulas de mariposa? </h2>
            <p> Si, sin embargo, le recomendamos que utilice los cuerpos de stock acelerador ya que estos son suficientes incluso para aplicaciones de alta potencia de arrastre. Cuerpos de mariposa alternos deben caracterizarse totalmente. </p>
            <h2> ¿Puedo cambiar los inyectores? </h2>
            <p> Sí, siempre y cuando se cambian las calibraciones de inyector para adaptarse. MoTeC ofrece calibraciones estándar. </p>
            <h2> ¿Tengo que volver a ajustar el motor después de un cambio de inyector? </h2>
            <p> Mientras las calibraciones de los inyectores son correctos, el sistema recalcula la entrega de combustible y sólo ajuste fino puede ser requerida. </p>
            <h2> ¿Puedo cambiar las bombas de combustible? </h2>
            <p>
            Sí, puntos de conmutación se pueden ajustar para adaptarse. Además, una bomba con control PID externa se puede utilizar, que requiere un MoTeC
            <a href="/dhb/dhboverview/" rel="external" target="_blank"> </a> DHB
            (Dual Medio puente) para accionar la bomba. La configuración de la bomba de combustible es muy flexible.
            <br>
            </p>
            <h2> ¿Puedo cambiar cámaras de distribución? </h2>
            <p> Sí. Un único parámetro de control debe ser cambiado y la eficiencia puede necesitar retuning. </p>
            <h2> ¿Puedo cambiar intercoolers? </h2>
            <p> Si, intercoolers de tipo cruzado son compatibles pero no se recomienda. </p>
            <h2> ¿Puedo cambiar la tubería intercooler? </h2>
            <p>
            Sí, cambia a
            <acronym title = "Unidad de Control del Motor"> ECU </acronym>
            ajustes deben ser innecesario.
            </p>
            <h2>
            ¿Puedo cambiar el
            <acronym title = "Flujo de Masa de Aire"> MAF </acronym>
            sensor o carcasa del sensor?
            </h2>
            <p> Sí, la calibración debe reflejar con precisión el comportamiento del sensor. </p>
            <h2> ¿Pueden eliminarse los sensores MAF? </h2>
            <p> Sí. El software se puede utilizar otra combinación de sensores de carga para hacer funcionar el motor. </p>
            <h2>
            ¿Puedo cambiar de impulso y
            <acronym title = "Presión absoluta del colector"> MAPA </acronym>
            sensores?
            </h2>
            <p> Sí, siempre y cuando la calibración refleja los sensores alterados. </p>
            <h2> ¿Puedo ejecutar inyectores de alta / baja? </h2>
            <p> Si, totalmente compatible. </p>
            <h2> ¿Puedo utilizar sensores de stock Lambda? </h2>
            <p>
            No, el kit incluye dos sensores de banda ancha y un NTK
            <acronym title = "Lambda a CAN dual"> LTCD </acronym>
            Controlador -NTK para mediciones precisas y repetibles.
            </p>
            <h2> ¿Puedo cambiar las fórmulas del combustible? </h2>
            <p> Sí, siempre y cuando la densidad de combustible y calibraciones combustible estequiométricas se cambian para adaptarse. Sensores de combustible Flex no están soportados en la versión inicial, pero puede ser en el futuro. </p>
            <h2> ¿Mi de stock cuentakilómetros y cuentakilómetros parcial en el trabajo? </h2>
            <p> Sí. </p>
            <h2> ¿Funciona el Control Cruise? </h2>
            <p> Sí, Control de crucero completo está incluido. </p>
            <h2> ¿Puedo utilizar este sistema con un motor VR38DETT independiente en otro vehículo? </h2>
            <p> Este firmware está diseñado para la integración del vehículo completo con el Nissan errores R35 GT-R y que, si no se detectan los sistemas de vehículos asociados. El motor se puede utilizar independiente, sin embargo, algunos subsistemas pueden no funcionar correctamente. </p>

