	  <h2> Control de Tracción ‡ </h2>
	  <p>
	  La opción de control de tracción permite tracción programable por el usuario y de control de lanzamiento. Control de tracción reduce la potencia del motor, mediante la reducción de los cilindros, para limitar el giro de la rueda cuando la tracción se rompe.
	  <br>
	  </p>
	  <h2> Hi / Lo Inyección ‡ </h2>
	  Hi / inyección Mín permite el control de los conjuntos primario y secundario de inyectores. Los inyectores son controlados usando una tabla 3D para definir el equilibrio de flujo de combustible entre los inyectores primarios y secundarios.
	  <br>
	  <h2> engranaje de cambio de encendido Cut ‡ </h2>
	  Engranaje Cambiar encendido Cut (también conocido como "cambio de plano ') corta momentáneamente la potencia del motor para permitir una velocidad hasta de cambio con el acelerador abierto. Una entrada a la
	  <acronym title = "Unidad de Control del Motor"> ECU </acronym>
	  se utiliza para activar el corte.
	  <br>
	  <h2> Superación Boost Mejora ‡ </h2>
	  <p>
	  Permite aumentar el control para reducir el retraso del turbo y mejorar la respuesta del acelerador en etapas estrecho y revirado para coches turboalimentados, específicamente en los eventos de rally.
	  <br>
	  </p>
	  <p> ‡ Control de Tracción, Hi / Lo de inyección, engranaje de cambio de encendido Cut y Mejora Boost Overrun están disponibles por separado o como parte de las funciones avanzadas. </p>
