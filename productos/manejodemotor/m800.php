	  </p>
	  </li>
	  <li>
	  Tabla Sitios del Eje
	  <br>
	  Los sitios de los ejes de la tabla pueden variar independientemente para casi todas las mesas, lo que permite la optimización completa de cada mesa.
	  </li>
