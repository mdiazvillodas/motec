	  <h2> Nuevo y mejorado las funciones de control </h2>
	  <ul>
	  <li>
	  <p>
	  control de Tracción
	  <br>
	  Las principales mejoras en el sistema de control de tracción que incluye la reducción de potencia a través de transmisión por hilo, corte de encendido y retardo del encendido. Incluye tres tablas 3D para deslizamiento objetivo que podría ser utilizado para el ajuste del controlador, la compensación de engranaje, compensación de la aceleración lateral o para el ajuste basado en cualquier otro canal.
	  </p>
	  </li>
	  <li>
	  <p>
	  Timers
	  <br>
	  Acelera a fondo y canales de temporizador sin pisar el acelerador más dos canales de temporizador de propósito general pueden ser utilizadas por cualquier mesa. Por ejemplo, una tabla de compensación de combustible o ignición.
	  </p>
	  </li>
	  <li>
	  <p>
	  Control de la velocidad de ralentí
	  <br>
	  Todo nuevo control de velocidad de ralentí, que incluye combustible y compensación de encendido mesas, además de tablas para establecer dicha marcha. Esto permite el ajuste RPM basado en un número de canales.
	  </p>
	  </li>
	  <li>
	  <p>
	  control de nitroso
	  <br>
	  Todo nuevo sistema de control nitroso mediante 3 etapas con atack y la decadencia, y un contador de tiempo de inicio de cada etapa. Las etapas pueden ser provocados por el acelerador, la velocidad del recorrido o entradas digitales.
	  </p>
	  </li>
	  <li>
	  <p>
	  control de refuerzo
	  <br>
	  Las principales mejoras para aumentar el control que incluye
	  <acronym title = "proporcional, ganancia Integral y Derivada"> PID </acronym>
	  de control con tablas 3D.
	  </p>
	  </li>
	  <li>
	  <p> Drive by Wire Control del acelerador </p>
	  </li>
	  <ul>
	  <li>
	  <li>
	  <p> Soporta hasta cuatro motores de accionamiento por cable del acelerador mediante un MoTeC DBW4. </p>
	  </li>
	  <li> Se puede utilizar para blip acelerador cambio descendente. </li>
	  </ul>
	  <li>
	  <p>
	  control de Lambda
	  <br>
	  Mejoras en el sistema de control Lambda incluyendo nuevo objetivo tablas de compensación Lambda que permiten que el objetivo Lambda a variar en función de cualquier canal. Por ejemplo, un canal de ajuste conductor o el canal completo temporizador del acelerador.
	  </p>
	  </li>
	  <li>
	  <p> Límite de RPM </p>
	  </li>
	  <ul>
	  <li>
	  <p> La tabla de límites de RPM 3D permite que el límite de RPM a variar en función de cualquier canal, por ejemplo engranajes o la temperatura del motor. </p>
	  </li>
	  <li> RPM cambio descendente limitante para que coincida con las RPM del motor a la velocidad de la caja de cambios. </li>
	  </ul>
	  </ul>
