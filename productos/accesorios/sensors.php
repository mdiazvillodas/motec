	  <h1 class = "bglightgrey">
	  ¿Cómo se mide la presión?
	  <br>
	  </h1>
	  <p class = "MsoNormal"> Sensores de presión trabajan sobre una base diferencial mediante la medición de la diferencia entre la presión aplicada y una presión de referencia. La referencia puede ser o bien la presión de vacío absoluto o presión atmosférica (aire ambiente). Por tanto, el sensor se dice para medir uno o presión 'absoluta' 'calibre'. </P>
	  <p class = "MsoNormal">
	  La presión atmosférica varía constantemente de un lugar a otro, a cada momento dependiendo de las condiciones climáticas del ambiente. La presión atmosférica estándar es aproximadamente igual a la presión de aire típica a nivel del mar y estandarizado a 101,325 kPa [psi o 0 (sg), 14,7 psi (a), 1,01 bar].
	  <br>
	  </p>
	  <h2>
	  Presión absoluta
	  <br>
	  </h2>
	  <p class = "MsoNormal">
	  Un sensor mide la presión absoluta de presión en relación con el vacío absoluto.
	  <br>
	  Un sensor celebrada en aire normal a nivel del mar mostrará alrededor de 101 kPa [psi 14.7 (a), 1,01 bar].
	  </p>
	  <p class = "MsoNormal">
	  Por ejemplo, la presión del colector (
	  <acronym title = "Presión absoluta del colector"> MAPA </acronym>
	  ) Sensores son sensores de presión absoluta, ya que se requieren estos sensores para medir las presiones tanto a continuación (vacío) y encima de la presión atmosférica.
	  </p>
	  <h2> Presión manométrica </h2>
	  <p class = "MsoNormal"> La presión puede medirse en relación a la presión atmosférica. Esto se conoce como presión manométrica y en el trabajo técnico, debe escribirse como psi (g). </p>
	  <p class = "MsoNormal">
	  Por ejemplo, la presión de aire en un neumático de un coche, lo que podría decirse que es '30 psi ', es en realidad 30 psi por encima de la presión atmosférica y se debe a que se refiere más exactamente a como 30 psi (g).
	  <br>
	  </p>
	  <h2> Manómetro sellado </h2>
	  <p class = "MsoNormal"> A veces la parte trasera de un sensor está sellado para evitar la contaminación y mejorar la repetibilidad de las lecturas. </p>
	  <p class = "MsoNormal">
	  El punto cero del sensor se ajusta a la presión atmosférica normal. Esto puede no ser exactamente la misma presión que cuando usted está en un día determinado, lo que resulta en lecturas de compensación. Los cambios de temperatura y presión atmosférica no afectará a la lectura de los sensores del calibrador sellados, es una medición precisa de la presión real de calibre superior a la atmosférica estándar.
	  <br>
	  </p>
	  <p class = "MsoNormal"> mediciones de calibre selladas se etiquetan como psi (s) o psi (sg). </p>
