	  <h3>
	  <a name="amplifiers"> </a>
	  Amplificadores de sensores magnéticos
	  </h3>
	  <p> El Convertidor magnética doble (DMC) convierte las señales de estilo magnéticas para abrir las señales de salida del colector - una onda cuadrada. Esto hace que comportan un sensor magnético típico como un sensor de efecto Hall o conmutada, para hacer las señales adecuado para uso con MoTeC ECUs y madereros. </p>
	  <p>
	  Esto permite que los sensores magnéticos que se utilizan en aplicaciones en las que previamente se requería un sensor Hall, por ejemplo cuando se utilizan las entradas digitales en una ECU para medir la velocidad de la rueda.
	  <br>
	  </p>
	  <p>
	  Una sola DMC puede convertir dos señales de los sensores magnéticos independientes.
	  <br>
	  </p>
	  <p>
	  La DMC viene en 4 versiones, cada una con diferentes niveles de activación de acuerdo con los requisitos de la aplicación prevista.
	  <br>
	  </p>
	  <ul>
	  <li>
	  DMC A
	  <br>
	  Esta versión está diseñada para sensores de caudal de combustible que tienen un nivel de activación muy bajo.
	  <br>
	  Filtrado pesado reduce al mínimo la posibilidad de interferencia, lo que resulta en una variación significativa del nivel de disparo con la frecuencia.
	  <br>
	  </li>
	  <li>
	  DMC B
	  <br>
	  Esta versión está diseñada para sensores de disparo del motor que tienen amplitud suficiente para desencadenar una entrada de nivel lógico.
	  <br>
	  Filtrado Minimal evita retrasos en la señal. El nivel de activación no varía significativamente en las frecuencias normales de funcionamiento.
	  <br>
	  </li>
	  <li>
	  DMC C
	  <br>
	  Esta versión está diseñada para acondicionadores de entrada del sistema de encendido, que se utilizan normalmente en las carreras de resistencia, para medir el tiempo de encendido.
	  <br>
	  </li>
	  <li>
	  DMC D
	  <br>
	  Esta versión está diseñada para los sensores de velocidad de las ruedas magnéticas.
	  <br>
	  La señal debe ser simétrica alrededor de cero voltios.
	  <br>
	  Filtrado pesado reduce al mínimo la posibilidad de interferencia, lo que resulta en una variación significativa del nivel de disparo con la frecuencia.
	  </li>
