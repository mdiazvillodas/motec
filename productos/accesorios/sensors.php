	  <h2>
	  <a name="Hall"> </a>
	  sensores Hall
	  </h2>
	  <p class = "Default"> sensores Hall contienen un circuito semiconductor de efecto Salón integrado (IC) y un imán. Hay tipos sensor Hall disponibles que se pueden utilizar con una rueda de gatillo diente (engranaje) de estilo para medir el ángulo de fisura o velocidad de la rueda, y los tipos que utilizan un rotor de paletas que pasa a través de un hueco sensor Hall. </P>
	  <p class = "default"> El IC detecta cambios en la intensidad de campo como la paleta pasa a través de la brecha de sensor o un diente pasa la punta del sensor. Los cambios de voltaje de salida de baja a alta (o alta a baja) cuando el borde delantero del diente pasa por el centro del sensor. </p>
	  <p class = "Default"> El material de hoja o el diente debe ser magnéticamente blandos, tales como acero dulce-no utilice acero inoxidable. </p>
