	  <h2>
	  <a name="magnetic"> </a>
	  Sensores magnéticos
	  </h2>
	  <p class = "Default"> Un sensor magnético consiste en una bobina arrollada de alambre que genera una tensión entre los cables del sensor cuando la intensidad del campo magnético cambia a medida que el diente pasa por el sensor. La amplitud de la tensión de salida depende de la brecha entre el sensor y los dientes y aumenta con el aumento de la velocidad más allá de la rueda de gatillo. </p>
	  <p class = "Default">
	  Sensores magnéticos pueden ser utilizados si la rueda de disparo tiene un gran número de dientes que el sensor tiene un tamaño pequeño polo magnético. A menudo se utilizan como sensores de cigüeñal o de velocidad de rueda.
	  <br>
	  </p>
	  <p class = "Default">
	  El sensor se puede conectar ya sea para una subida o bajada de forma de onda mediante la inversión de los cables. El
	  <acronym title = "Unidad de Control del Motor"> ECU </acronym>
	  debe ser configurado en consecuencia. El material de los dientes debe ser magnéticamente blando, tal como acero dulce (no utilizar acero inoxidable).
	  </p>
	  <p class = "Default">
	  Sensores magnéticos deben utilizar cables blindados para ayudar a eliminar el ruido eléctrico indeseado y debe ser montado de forma rígida como cualquier vibración puede causar señales falsas.
