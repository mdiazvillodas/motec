	  Módulos de Knock de MoTeC proporcionan una de las botellas de lazo cerrado de control de golpe, lo que permite sintonizadores para optimizar de forma segura mapas de encendido de alto rendimiento. Hay un módulo independiente y una versión específicamente diseñada para su montaje en una
