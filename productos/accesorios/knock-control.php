	  <p> Knock (también llamados "detonación") es una forma irregular de la combustión que puede causar fallo de motor catastrófico. La detonación se produce cuando el calor excesivo y la presión en la cámara de combustión hace que el / mezcla de aire y combustible para encender auto. Esto produce un aumento súbito de la presión del cilindro acompañado de un agudo y metálico ping o golpeando ruido. Las ondas de choque de martillo como creados por la detonación sujeta la junta de culata, pistones, anillos, bujía y cojinetes de biela de sobrecarga severa. Detonación leve u ocasional puede ocurrir en casi cualquier motor ya menudo no causa ningún daño. Pero detonación prolongado o pesado puede ser muy perjudicial. </P>
	  <p class = "MsoNormal"> La detonación provoca tres tipos de fallas: </p>
	  <ul>
	  <li> Los daños mecánicos (tierras anillo roto, martillado cojinetes de cabeza) </li>
	  <li> abrasión (picaduras de la cabeza del pistón) </li>
	  <li>
	  Sobrecalentamiento (rayado faldas del pistón debido a la entrada de calor en exceso o elevadas temperaturas de refrigeración)
