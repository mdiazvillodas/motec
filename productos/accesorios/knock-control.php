	  <p class = "MsoNormal"> Para configurar un sistema de control de detonación electrónica confiable es necesario para determinar los niveles de detonación con exactitud y diferenciar golpe del ruido normal del motor. La mayoría de los motores modernos están equipados con un sensor de detonación piezoeléctrico. Módulos de Knock de MoTeC utilizan estos sensores para medir el ruido del motor y detectar los niveles de detonación mediante la reducción de la influencia de ruido del motor de fondo utilizando una combinación de gating datos y filtrado de frecuencia. </p>
	  <p class = "MsoNormal"> Un sintonizador competente debe llevar a cabo un análisis de frecuencia para determinar la frecuencia objetivo y golpear configuración del módulo. Este es un trabajo altamente especializado y por lo tanto la MoTeC de Knock módulos sólo estará disponible a través de distribuidores autorizados que han emprendido una formación específica. </P>
	  <p class = "MsoNormal"> Cuando se determinan los ajustes de Módulos de Knock, la ECU puede ser configurado para monitorear los niveles de detonación y para retardar el tiempo de encendido de un cilindro individual o de todos los cilindros si el nivel de detonación excede un valor predeterminado, protegiendo así el motor contra la detonación excesiva. </p>
