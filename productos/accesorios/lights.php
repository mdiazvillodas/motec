	  <p> Proporciona ocho LED multicolores luces indicadoras en un conjunto compacto. El color, la intensidad y el propósito de cada LED se puede controlar de forma independiente, proporcionando una gran flexibilidad y posibilidades de personalización. </p>
	  <p> Un conjunto de patrones se puede definir con varias combinaciones de colores y modos que destellan, indicando por ejemplo: detener inmediatamente, continúe con precaución, pit lane limitador de velocidad activo y, por supuesto, hasta de cambio y cambio descendente. Los LEDs pueden ser programados en un orden de prioridad a fin de que, por ejemplo, una luz de advertencia puede anular un cambio de luz. </P>
	  <p>
	  El MST se configura y controla el uso de un MoTeC
	  <acronym title = "Unidad de Control del Motor"> ECU </acronym>
