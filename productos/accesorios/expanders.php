	  <p class = "MsoNormal"> El módulo de entrada síncrono Versátil (SVIM) es un expansor compacto que trabaja en conjunto con una ACL o ADL3 Data Logger para facilitar el registro sincronizado de alta velocidad, entradas de alta resolución. </p>
	  <p class = "MsoNormal"> El SVIM adquiere datos de la más alta integridad para su uso en chasis de avanzada y análisis de suspensión. Se muestras 18 entradas en el mismo instante en el tiempo usando 18 convertidores separados con quinto orden filtros anti-aliasing. </P>
	  <p class = "MsoNormal"> Múltiples SVIMs están sincronizados de manera que cada SVIM en las muestras de los vehículos de sus entradas de AV de alta resolución en el mismo instante en el tiempo. Esto es importante cuando se mira en las relaciones de las señales procedentes de diferentes sensores alrededor del vehículo y es útil para los bancos de pruebas y simulaciones en tiempo real. </P>
	  <p class = "MsoNormal"> SVIMs son versátiles en la naturaleza y puede ser situado cerca de los sensores, lo que reduce el peso y la complejidad de cableado. Cuando se utiliza en múltiplos, hasta 200 entradas de sensor están disponibles. El SVIM compatible con muchos tipos diferentes de sensores, incluyendo termopares unamplified y extensómetros. </P>
	  <p class = "MsoNormal">
	  El SVIM se configura y controla mediante el software de Data Logger, que tiene funciones de programación especiales para la SVIM. Esto también se actualizará automáticamente la versión del firmware en el SVIM si es necesario. El registrador de datos se comunica a la SVIM a través de la
	  <acronym title = "Controller Area Network - protocolo de comunicación"> CAN </acronym>
	  autobús.
